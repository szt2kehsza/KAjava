public class TwoNumbers {
    public static void main(String[] args) {

        int a = 22;
        int b = 13;

        // 1
        int sum = a + b;
        System.out.println("1.: "+ sum);

        // 2
        int substracted = a - b;
        System.out.println("2.: "+ substracted);

        // 3
        int multiplied = a * b;
        System.out.println("3.: "+ multiplied);

        // 4
        double divided_a_decimal_fraction = (double) a / b;
        System.out.println("4.: "+ divided_a_decimal_fraction);

        // 5
        int divided = a / b;
        System.out.println("5.: "+ divided);

        // 6
        int remainder_divided = a % b;
        System.out.println("6.: "+ remainder_divided);

    }
}
